export type Locale = 'vi' | 'en';

export function t(locale: Locale, key: string): string {
  const keys = key.split('.');
  let value: any = translations[locale];

  for (const k of keys) {
    value = value[k];
    if (value === undefined) {
      return key; // Return key if translation not found
    }
  }

  return value;
}
